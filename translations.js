let currentAppLang = localStorage.getItem('app_lang') || 'en';

function initGlobalLanguage() {
    // 1. Inject Modal if not exists
    if (!document.getElementById('langModal')) {
        injectLanguageModal();
    }

    // 2. Inject Button into Profile Dropdown (if dropdown exists)
    // We try to find #profileDropdown and prepend/append language option
    // BUT typically profile dropdowns are just links. We want it maybe before Logout.
    const dropdown = document.getElementById('profileDropdown');
    if (dropdown && !document.getElementById('langDropdownItem')) {
        const langLink = document.createElement('a');
        langLink.id = 'langDropdownItem';
        langLink.href = '#';
        langLink.innerHTML = `<span data-i18n="language">Language</span>`;
        langLink.onclick = (e) => {
            e.preventDefault();
            e.stopPropagation();
            openGlobalLanguageModal();
            // Close dropdown if needed? kept open usually fine, or close it:
            if (dropdown.classList.contains('show')) dropdown.classList.remove('show');
        };

        // Insert before Logout (usually last child)
        if (dropdown.lastElementChild) {
            dropdown.insertBefore(langLink, dropdown.lastElementChild);
        } else {
            dropdown.appendChild(langLink);
        }
    }

    // 3. Inject Feedback
    if (dropdown && !document.getElementById('feedbackDropdownItem')) {
        const fbLink = document.createElement('a');
        fbLink.id = 'feedbackDropdownItem';
        fbLink.href = 'feedback.html';
        fbLink.innerHTML = '<span data-i18n="feedback">Feedback</span>';
        if (dropdown.lastElementChild) {
            dropdown.insertBefore(fbLink, dropdown.lastElementChild);
        } else {
            dropdown.appendChild(fbLink);
        }
    }

    // 4. Apply translations
    applyGlobalTranslations();
}

function injectLanguageModal() {
    const modalHTML = `
    <div class="sidebar-overlay" id="globalLangOverlay" onclick="closeGlobalLanguageModal()" style="z-index: 2000;"></div>
    <div id="globalLangModal"
        style="display:none; position:fixed; top:50%; left:50%; transform:translate(-50%, -50%); background:#fff; padding:24px; border-radius:16px; width:90%; max-width:400px; z-index:2001; box-shadow:0 10px 40px rgba(0,0,0,0.3); font-family:system-ui, sans-serif;">
        <h3 id="langTitle" style="margin-bottom:16px; text-align:center; color:#333;">Select Language</h3>
        <div id="globalLangList"
            style="display:grid; grid-template-columns:1fr 1fr; gap:12px; max-height:400px; overflow-y:auto;">
            <!-- Populated by JS -->
        </div>
        <button onclick="closeGlobalLanguageModal()" style="margin-top:20px; background:#eee; color:#333; width:100%; padding:10px; border:none; border-radius:8px; cursor:pointer; font-weight:600;">Close</button>
    </div>
    `;
    document.body.insertAdjacentHTML('beforeend', modalHTML);
}

function openGlobalLanguageModal() {
    const list = document.getElementById('globalLangList');
    list.innerHTML = '';

    languages.forEach(l => {
        const btn = document.createElement('div');
        btn.style.padding = '10px';
        btn.style.border = '1px solid #eee';
        btn.style.borderRadius = '8px';
        btn.style.cursor = 'pointer';
        btn.style.textAlign = 'center';
        btn.style.fontSize = '0.9rem';
        btn.textContent = l.name;
        btn.onclick = () => changeGlobalLanguage(l.code);

        if (l.code === currentAppLang) {
            btn.style.background = '#fff7ee';
            btn.style.borderColor = '#d2b89b';
            btn.style.fontWeight = 'bold';
        }

        list.appendChild(btn);
    });

    document.getElementById('globalLangOverlay').style.display = 'block';
    document.getElementById('globalLangModal').style.display = 'block';
}

function closeGlobalLanguageModal() {
    document.getElementById('globalLangOverlay').style.display = 'none';
    document.getElementById('globalLangModal').style.display = 'none';
}

function changeGlobalLanguage(code) {
    currentAppLang = code;
    localStorage.setItem('app_lang', code);
    closeGlobalLanguageModal();
    applyGlobalTranslations();
    window.dispatchEvent(new Event('languageChanged'));
}

function applyGlobalTranslations() {
    const dict = translations[currentAppLang] || translations['en'];
    const enDict = translations['en']; // Fallback

    // 1. Text Content: data-i18n="key"
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = dict[key] || enDict[key];
        if (val) {
            el.textContent = val;
        }
    });

    // 2. Placeholders: data-i18n-placeholder="key"
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const val = dict[key] || enDict[key];
        if (val) {
            el.placeholder = val;
        }
    });

    // 3. Special: Profile Button text if it exists separately? 
    // Usually handled by data-i18n w/ valid keys.
}

// Helper for JS-based translations
function t(key) {
    const dict = translations[currentAppLang] || translations['en'];
    const enDict = translations['en'];
    return dict[key] || enDict[key] || key;
}

